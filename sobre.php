          <h2>Sobre o Site Comunitário</h2>
          
          <p align="justify" style="text-indent:30px;">

          O Site Comunitário é uma pagina que oferece caminhos para alunos & Oferecer caminhos é Ensinar.<br/><br/>

    Cada período escolar de um aluno procura novos metodos para preparar-se para a vida adulta, que se traduz na postura, no comportamento e no conhecimento adquirido.

    Uma formação integral, com o desenvolvimento harmonioso dos potenciais da criança e do jovem, obtido por meio de técnicas didáticas e estratégias de aulas atuais e incentivadoras do trabalho escolar.
    É esta a função do Scom(Site Comunitário), este da suporte de 88% na escolhas de cursos a fazer e em que institutos fazer.

